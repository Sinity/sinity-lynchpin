def _out(name: str) -> str:
    return resolve_analysis_path(name)


def _current_state_refresh_id(
    *,
    start: date,
    end: date,
    projects: Sequence[str] | None,
) -> str:
    project_key = ",".join(sorted(projects or ())) if projects else "all"
    return f"current-state:{start.isoformat()}:{end.isoformat()}:{project_key}"


def _machine_analysis_refresh_id(
    *,
    start: date | None,
    end: date | None,
) -> str:
    start_key = start.isoformat() if start is not None else "rolling"
    end_key = end.isoformat() if end is not None else "today"
    return f"machine-analysis:{start_key}:{end_key}"


def _rolling_window(
    *,
    start: date | None,
    end: date | None,
    days: int,
) -> tuple[date, date]:
    """Resolve an optional inclusive rolling analysis window."""
    resolved_end = end or date.today()
    resolved_start = start or (resolved_end - timedelta(days=days - 1))
    return resolved_start, resolved_end


def _promote_operator_day(*, start: date | None, end: date | None) -> dict:
    from lynchpin.substrate.connection import apply_schema, connect, substrate_path
    from lynchpin.substrate.personal import promote_operator_day_rows
    from lynchpin.analysis.active.substrate_promote_status import record_source_status

    resolved_start, resolved_end = _rolling_window(start=start, end=end, days=90)
    refresh_id = f"operator-day:{resolved_start.isoformat()}:{resolved_end.isoformat()}"
    rows = operator_daily_matrix(resolved_start, resolved_end, skip_slow=True)
    with connect(substrate_path()) as conn:
        apply_schema(conn)
        n = promote_operator_day_rows(conn, refresh_id=refresh_id, rows=rows)
        record_source_status(
            conn,
            refresh_id=refresh_id,
            source="operator_day",
            status="ok" if n > 0 else "empty",
            reason=None,
            row_count=n,
            window_start=resolved_start,
            window_end=resolved_end,
        )
    return {"refresh_id": refresh_id, "rows_promoted": n}


