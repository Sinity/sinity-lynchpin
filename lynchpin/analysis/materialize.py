def machine_analysis_dag(
    *,
    start: date | None = None,
    end: date | None = None,
) -> DAG:
    """Build the source-selected machine-analysis materialization DAG."""
    dag = DAG("machine-analysis-materialization")
    machine_start, machine_end = _rolling_window(start=start, end=end, days=90)
    dag.add(Step(
        "machine_analysis_substrate_promote",
        fn=lambda: run_substrate_promote(
            commit_facts_file=_out("active_commit_facts.json"),
            file_changes_file=_out("active_file_change_facts.json"),
            symbol_changes_file=_out("active_symbol_changes.json"),
            pr_review_file=_out("active_pr_review_topology.json"),
            refresh_id=_machine_analysis_refresh_id(start=start, end=end),
            window_start=machine_start,
            window_end=machine_end,
            sources=MACHINE_ANALYSIS_SUBSTRATE_SOURCES,
            write_evidence_graph=False,
        ),
    ))
    _add_machine_analysis_steps(
        dag,
        start=start,
        end=end,
        substrate_step="machine_analysis_substrate_promote",
    )
    return dag


def _run_narrative(
    *,
    start: date,
    end: date,
    projects: Sequence[str] | None = None,
) -> dict[str, Any]:
    from ..graph.narrative import narrate
    narrate(
        start=start, end=end, projects=projects,
        out=str(resolve_analysis_path("current_state_narrative.md")),
        json_out=str(resolve_analysis_path("current_state_narrative.json")),
    )
    return {"status": "ok"}


def _compute_sinex_temporal(repo: str, out_path: str) -> object:
    monthly = sinex_temporal.compute_monthly_velocity(repo)
    crate_growth = sinex_temporal.compute_crate_growth(repo)
    stats = sinex_temporal.compute_sinex_stats(repo)
    payload = {
        'monthly_velocity': monthly,
        'crate_growth': crate_growth,
        'stats': stats,
    }
    save_json(resolve_analysis_path(out_path), payload)
    return payload
