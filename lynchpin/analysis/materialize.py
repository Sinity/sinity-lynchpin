def _code_version() -> str:
    """A token that changes when the analysis code changes.

    Under the Nix deployment the package lives at a content-addressed store
    path, so its parent directory is unique per build; in a dev checkout it is
    stable, so use ``materialize --no-memo`` to force a rebuild after editing
    analysis code.
    """

    import lynchpin

    return str(Path(lynchpin.__file__).resolve().parent)


